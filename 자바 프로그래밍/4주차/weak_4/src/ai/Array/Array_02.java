package ai.Array;

public class Array_02 {

	public static void main(String[] args) {
		int num[] = {10,20,30,40};
		char ch[] = {'a','b','c'};
		String fruit[] = {"사과","딸기","바나나"};
		
		for(int nl:num) System.out.print(nl + " ");
		System.out.println();
		for(char cl:ch) System.out.print(cl + " ");
		System.out.println();
		for(String fl:fruit) System.out.print(fl + " ");
		System.out.println();
	}

}
